package hw_8_4;

public class rectangle

{
public int length = 1;
 public int width = 1;




public int  perimiter (int length, int width)
{
	int perimiter;
	
	perimiter = 2 * (length + width);
	
	
	return perimiter;
}


public int area(int length,int width)
{
	
	int area;
	area = length * width;
	return area;
}
}
